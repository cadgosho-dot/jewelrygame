export const DAILY_GEM_IDS = Object.freeze(["diamond", "ruby", "sapphire", "emerald", "aquamarine", "morganite", "alexandrite", "chrysoberylCatseye", "spinel", "zircon", "tanzanite", "iolite", "kunzite", "imperialTopaz", "tourmaline", "paraiba", "garnet", "demantoid", "tsavorite", "peridot", "amethyst", "citrine", "opal", "moonstone", "jadeite", "nephrite", "turquoise", "lapis", "pearl", "amber"]);

export function dailyGemForDate(dateLike) {
  const source = dateLike instanceof Date ? dateLike : new Date(dateLike);
  const month = Number.isNaN(source.getTime()) ? 1 : source.getMonth() + 1;
  const day = Number.isNaN(source.getTime()) ? 1 : source.getDate();
  // 月日だけで固定される整数ハッシュ。年が変わっても同じ月日は同じ宝石になる。
  let seed = (month * 100 + day) >>> 0;
  seed ^= seed << 13; seed >>>= 0;
  seed ^= seed >>> 17; seed >>>= 0;
  seed ^= seed << 5; seed >>>= 0;
  const id = DAILY_GEM_IDS[seed % DAILY_GEM_IDS.length];
  return { id, ...DAILY_GEM_PROFILES[id] };
}
